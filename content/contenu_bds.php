     <h3>Le BDS en un mot:</h3>
     <p>Constituée depuis 2013 d'un bureau de 5 élèves et d'un Conseil d'Administration d'un représentant par section, l'ASEP a pour vocation d'encourager la pratique sportive des X, à travers l'organisation d’événements sportifs, la subvention des compétitions.</p>

     <h3>Le mot du prez: </h3>
     <p> Bienvenue sur le site du Bureau des Sports de l'Ecole Polytechnique. <br><br>

         Le BDS est constitué d'une douzaine d'étudiants en deuxième année à l'X, et d'un représentant par sport.

         Notre mission est de faire vivre le sport, sur notre campus et à l'extérieur, par exemple via les grandes rendez-vous sportifs auxquels les polytechnicien(ne)s participent tout au long de l'année).

         Les X des différentes promotions peuvent retrouver le planning des compétitions dans lesquelles Polytechnique est engagé, et consulter les classements des équipes.

         De nombreux évènements sportifs (nuits sportives, 24h de la natation, jumping, ...) sont organisés par les polytechniciens et figurent sur l'agenda de ce site. Certains sont ouverts aux étudiants extérieurs, avis aux amateurs !

         <br>Pour tout renseignement ou suggestion, n'hésitez pas à nous contacter !

         <br><br>
         Sportivement,

         <br> <br>Arthur OLLIVIER </p>
